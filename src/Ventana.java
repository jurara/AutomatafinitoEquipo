    public boolean jTableValidoAFN() {

        for (int j = 0; j < jTable1.getRowCount(); j++) {
            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                //            JOptionPane.showMessageDialog(this, jTable1.getModel().getValueAt(j, i).toString() + "putos todos");
                if (jTable1.getModel().getValueAt(j, i) == null) {
                    return false;
                } else {

                    if (jTable1.getModel().getValueAt(j, i).toString().compareTo("") == 0) {
                        return false;
                    } else {
                        if (jTable1.getModel().getValueAt(j, i).toString().compareTo("-") == 0) {
                            return true;
                        } else {

                            if (comaSeguida(jTable1.getModel().getValueAt(j, i).toString())) {
                                return false;
                            } else {
                                String ar[] = jTable1.getModel().getValueAt(j, i).toString().split(",");
                                for (int k = 0; k < ar.length; k++) {
                                    if (!estaEnEst(ar[k])) {
                                        return false;
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
        return true;
    }

