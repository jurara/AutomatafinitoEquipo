    public boolean comaSeguida(String cad) {

        int cont = 0;
        if (cad.charAt(0) == ',') {
            return true;
        }
        if (cad.charAt(cad.length() - 1) == ',') {
            return true;
        }
        for (int i = 0; i < cad.length(); i++) {
            if (cad.charAt(i) == ',') {
                cont++;
            } else {
                cont = 0;
            }

            if (cont > 1) {
                return true;
            }


        }
        return false;
    }

