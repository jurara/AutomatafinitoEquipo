    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (cajasVacias()) {
            JOptionPane.showMessageDialog(this, "Favor de llenar todos los campos");
        } else {
            if (!esAlf(alfTxt.getText().split(","))) {
                JOptionPane.showMessageDialog(this, "alfabeto invalido");
            } else {
                if (!comaSeguida(estTxt.getText())) {
                    alf = ("Q," + alfTxt.getText()).split(",");

                    est = estTxt.getText().split(",");
                    if (estValido(finTxt.getText()) && estValido(iniTxt.getText()) && estValido(iniTxt.getText()) && iniTxt.getText().indexOf(",") == -1) {
                        alfTxt.setEnabled(false);
                        estTxt.setEnabled(false);
                        iniTxt.setEnabled(false);
                        finTxt.setEnabled(false);
                        cadTxt.setEnabled(true);
                        cadTxt.setEnabled(true);
                        btnIni.setVisible(true);
                        lblCad.setVisible(true);
                        cadTxt.setVisible(true);



                        String matrizTabla[][] = new String[est.length][alf.length];
                        for (int i = 0; i < est.length; i++) {
                            matrizTabla[i][0] = est[i];

                        }
                        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                                matrizTabla,
                                alf));
                    } else {
                        JOptionPane.showMessageDialog(this, "No concuerdan los estados");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Estados mal escritos");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cadTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadTxtActionPerformed

        if (determinista) {
            if (jTableValidoAFD()) {
                if (analizador()) {
                    resLbl.setText("   Cadena valida");
                    resLbl.setBackground(Color.white);
                } else {
                    resLbl.setText("   Cadena no valida");
                    resLbl.setBackground(Color.red);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Hay errores en la tabla de estados");
            }
        } else {
            if (jTableValidoAFN()) {
                if (analizador()) {
                    resLbl.setText("   Cadena válida");
                    resLbl.setBackground(Color.white);
                } else {
                    resLbl.setText("   Cadena no válida");
                    resLbl.setBackground(Color.red);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Hay errores en la tabla de estados");
            }
        }
    }//GEN-LAST:event_cadTxtActionPerformed

